<script>
// Score Calculator Modal
document.addEventListener('DOMContentLoaded', function() {
    const modal = document.getElementById('scoreCalculatorModal');
    const calculateBtns = document.querySelectorAll('.btn-success');
    const closeBtn = document.querySelector('.close');
    const form = document.getElementById('scoreCalculatorForm');
    const scoreResult = document.getElementById('scoreResult');

    calculateBtns.forEach(btn => {
        if (btn.textContent.trim() === 'Calculate Score') {
            btn.addEventListener('click', function(e) {
                e.preventDefault();
                modal.style.display = 'block';
            });
        }
    });

    closeBtn.addEventListener('click', function() {
        modal.style.display = 'none';
        scoreResult.classList.remove('show');
        form.reset();
    });

    window.addEventListener('click', function(e) {
        if (e.target === modal) {
            modal.style.display = 'none';
            scoreResult.classList.remove('show');
            form.reset();
        }
    });

    form.addEventListener('submit', function(e) {
        e.preventDefault();
        
        const exam = form.exam.value;
        const correct = parseInt(form.correct.value) || 0;
        const wrong = parseInt(form.wrong.value) || 0;
        const unattempted = parseInt(form.unattempted.value) || 0;
        
        // Sample calculation (adjust based on exam pattern)
        let positiveMarks = correct * 2; // 2 marks per correct answer
        let negativeMarks = wrong * 0.5; // 0.5 negative marking
        let totalScore = positiveMarks - negativeMarks;
        let totalQuestions = correct + wrong + unattempted;
        let percentage = totalQuestions > 0 ? ((correct / totalQuestions) * 100).toFixed(2) : 0;
        
        scoreResult.innerHTML = `
            <h4>Your Calculated Score</h4>
            <div class="score-breakdown">
                <div class="score-item">
                    <div class="score-number">${correct}</div>
                    <div class="score-label">Correct</div>
                </div>
                <div class="score-item">
                    <div class="score-number">${wrong}</div>
                    <div class="score-label">Wrong</div>
                </div>
                <div class="score-item">
                    <div class="score-number">${unattempted}</div>
                    <div class="score-label">Unattempted</div>
                </div>
                <div class="score-item total">
                    <div class="score-number">${totalScore}</div>
                    <div class="score-label">Total Score</div>
                </div>
            </div>
            <div style="text-align: center; margin-top: 15px;">
                <p><strong>Accuracy: ${percentage}%</strong></p>
                <p><em>Note: This is an estimated score. Actual results may vary based on official marking scheme.</em></p>
            </div>
        `;
        
        scoreResult.classList.add('show');
    });
});
</script>

